package com.machineAdmin.entities.postgres;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.Size;


/**
 *
 * @author Ulises Beltr치n G칩mez --- beltrangomezulises@gmail.com
 */
@Entity
@Table(name = "ejemplo")

@NamedQueries({
    @NamedQuery(name = "Ejemplo.findAll", query = "SELECT e FROM Ejemplo e")
    , @NamedQuery(name = "Ejemplo.findById", query = "SELECT e FROM Ejemplo e WHERE e.id = :id")
    , @NamedQuery(name = "Ejemplo.findByDes", query = "SELECT e FROM Ejemplo e WHERE e.des = :des")})
public class Ejemplo implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Size(max = 2147483647)
    @Column(name = "des")
    private String des;

    public Ejemplo() {
    }

    public Ejemplo(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Ejemplo)) {
            return false;
        }
        Ejemplo other = (Ejemplo) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.machineAdmin.entities.postgres.Ejemplo[ id=" + id + " ]";
    }
    
}
